    // JVM (Java Virtual Machine): The engine that actually runs your code. It reads compiled Java bytecode and translates it into machine-specific instructions. It is the core reason Java is platform-independent.

    // JRE (Java Runtime Environment): The environment needed to run Java applications. It contains the JVM plus core class libraries (like java.lang, java.util) and other supporting files. If you only want to run a Java program, you just need the JRE.

    // JDK (Java Development Kit): The full toolkit needed to develop Java applications. It includes the JRE, plus development tools like the compiler (javac), the debugger, and packaging tools (jar).

    // Unlike languages like Python (which is interpreted) or C++ (which is compiled directly to machine code), Java uses a two-step process:

    // Compilation (javac): You write human-readable source code in a .java file. The Java compiler (javac) checks for syntax errors and translates this code into an intermediate language called bytecode, saving it as a .class file.

    // Execution (java): The JVM takes this .class file and translates the bytecode into machine code that your specific operating system (like Fedora, Windows, or macOS) can understand.

    // Note: This two-step process is what gives Java its "Write Once, Run Anywhere" (WORA) capability.

